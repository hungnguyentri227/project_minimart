    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        String id = txtid.getText();
        String firstname = txtfirstnae.getText();
        String lastname = txtlastname.getText();
        String tel = txttel.getText();
        String email = txtemail.getText();

        CustomerDTO dto = new CustomerDTO(id, firstname, lastname, tel, email, true);
        if (dao.check_Insert(id, firstname, lastname, tel, email)) {
            boolean result = dao.Insert(dto);
            if (result) {
                JOptionPane.showMessageDialog(this, "Insert Customer Success.");

            } else {
                JOptionPane.showMessageDialog(null, "Insert Customer Not Success.", "Error.", 0);
            }
        }
        loadData();
        //txtid.setEditable(false);
    }//GEN-LAST:event_btninsertActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try {
            String id = txtid.getText();
            String firstname = txtfirstnae.getText();
            String lastname = txtlastname.getText();
            String tel = txttel.getText();
            String email = txtemail.getText();

            CustomerDTO dto = new CustomerDTO(id, firstname, lastname, tel, email, true);
            if (dao.check_Insert(id, firstname, lastname, tel, email)) {
                boolean result = dao.Update(dto);
                if (result) {
                    JOptionPane.showMessageDialog(this, "Update Customer Success.");

                } else {
                    JOptionPane.showMessageDialog(null, "Update Customer Not Success.", "Error.", 0);
                }
            }
        } catch (Exception e) {
        }

        loadData();
    }//GEN-LAST:event_btneditActionPerformed

    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        // TODO add your handling code here:
        String id = txtid.getText();
        String firstname = txtfirstnae.getText();
        String lastname = txtlastname.getText();
        String tel = txttel.getText();
        String email = txtemail.getText();

        CustomerDTO dto = new CustomerDTO(id, firstname, lastname, tel, email, false);
        if (dao.check_Insert(id, firstname, lastname, tel, email)) {
            boolean result = dao.Delete(dto);
            if (result) {
                JOptionPane.showMessageDialog(this, "Delete Customer Success.");

            } else {
                JOptionPane.showMessageDialog(null, "Delete Customer Not Success.", "Error.", 0);
            }
        }
        loadData();
    }//GEN-LAST:event_btnremoveActionPerformed

