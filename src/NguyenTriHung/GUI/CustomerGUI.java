    public void loadData() {
        tblmodel.setRowCount(0);
        Vector<CustomerDTO> dto = dao.showDetails();
        for (CustomerDTO customerDTO : dto) {
            tblmodel.addRow(customerDTO.toVector());
        }
    }
    

