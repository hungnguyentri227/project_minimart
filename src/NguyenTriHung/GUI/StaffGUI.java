    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        String id = txtid.getText();
        String username = txtusername.getText();
        String password = String.valueOf(txtpassword.getText());
        String fullname = txtfullname.getText();
        String tel = txtTel.getText();
        String email = txtemail.getText();
        String role = cbrole.getSelectedItem().toString();

        userDTO dto = new userDTO(id, username, password, fullname, tel, email, role, true);
        if (dao.Validation_Dangky(id, username, password, fullname, tel, email, role)) {
            if (dao.Insert(dto)) {
                JOptionPane.showMessageDialog(null, "Insert Success.");
            } else {
                JOptionPane.showMessageDialog(null, "Insert Not Success.", "Error!", 0);
            }
        }
        loadData();
        txtid.setEditable(false);
        txtusername.setEditable(false);
    }//GEN-LAST:event_btninsertActionPerformed

