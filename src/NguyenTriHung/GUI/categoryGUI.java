    public void loadData() {
        tblModel.setRowCount(0);
        Vector<categoryDTO> dto = dao.showDetails();
        for (categoryDTO dTO : dto) {
            tblModel.addRow(dTO.toVector());
        }
        txtID.setEnabled(false);
    }

    public void loadData2(String category) {
        tblModel2.setRowCount(0);
        Vector<productDTO> dto = dao.SelectDataCategory(category);
        jLabel3.setText("PRODUCT IN THE CATEGORY: " + category);
        for (productDTO dTO : dto) {
            tblModel2.addRow(dTO.toVector());
        }
    }

