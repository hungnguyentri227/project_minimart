    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int ID = Integer.parseInt(txtID.getText());
            String name = txtname.getText();
            categoryDTO dto = new categoryDTO(ID, name, true);
            if (dao.check_category(ID, name)) {
                if (dao.Insert(dto)) {
                    JOptionPane.showMessageDialog(null, "Insert Category Success");
                } else {
                    JOptionPane.showMessageDialog(null, "Insert Category NOT Success", "Error!", 0);
                }
            }
            loadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Không để trống ID và viết đúng cú pháp.", "Error!", 0);
        }
        txtID.setEnabled(false);


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int ID = Integer.parseInt(txtID.getText());
            String name = txtname.getText();
            categoryDTO dto = new categoryDTO(ID, name, true);
            if (dao.check_category(ID, name)) {
                if (dao.Edit(dto)) {
                    JOptionPane.showMessageDialog(null, "Update Category Success");
                } else {
                    JOptionPane.showMessageDialog(null, "Update Category NOT Success", "Error!", 0);
                }
            }
        } catch (Exception e) {
        }
        loadData();

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            int ID = Integer.parseInt(txtID.getText());
            String name = txtname.getText();

            categoryDTO dto = new categoryDTO(ID, name, false);
            if (dao.findProduct_to_Category(name) != null) {
                JOptionPane.showMessageDialog(null, "Tồn tại Product chứa Category.", "Error.", 0);
            } else {

                if (dao.check_category(ID, name)) {
                    if (dao.Delete(dto)) {
                        JOptionPane.showMessageDialog(null, "Delete Category Success");
                    } else {
                        JOptionPane.showMessageDialog(null, "Delete Category NOT Success", "Error!", 0);
                    }
                }
            }

            loadData();
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton3ActionPerformed

